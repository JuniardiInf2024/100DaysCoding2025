package juniardi;

public class Coding5 {
    public static void main(String[] args) {
        byte angka_byte = 27; // byte range = -128 sd 127
        short angka_short = 6;// short range = -32.768 sd 32.767
        int angka_int = 2005; // int range = -2^31 sd 2^31-1
        long angka_long = 270605; // long range = -2^63 sd 2^63-1

        System.out.println(angka_byte);
        System.out.println(angka_short);
        System.out.println(angka_int);
        System.out.println(angka_long);
    }
}
