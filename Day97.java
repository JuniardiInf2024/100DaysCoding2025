public class Coding97 {
    static int luasPersegi(int sisi) {
        return sisi * sisi;
    }
    public static void main(String[] args) {
        int s = 2;
        int hasil = luasPersegi(s);

        System.out.println("Nilai sisi   : " + s);
        System.out.println("Luas persegi : " + hasil);
    }
}
