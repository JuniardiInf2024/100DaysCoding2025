package juniardi;
public class Coding26{
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println("Nilai awal:");
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        a += b;
        System.out.println("a += b: " + a);

        a -= b;
        System.out.println("a -= b: " + a);

        a *= b;
        System.out.println("a *= b: " + a);

        a /= b;
        System.out.println("a /= b: " + a);

        a %= b;
        System.out.println("a %= b: " + a);
    }
}
