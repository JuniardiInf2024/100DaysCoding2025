package juniardi;

public class Coding3 {
    public static void main(String[] args) {
        // \n = membuat baris baru
        System.out.println("Halo, nama saya Juniardi\nSaya sedang belajar Java.");

        // \t = memberi tab (spasi lebar)
        System.out.println("Nama:\tJuniardi");
        System.out.println("Kelas:\tInformatika 2025");

        // \" = menampilkan tanda kutip ganda
        System.out.println("Saya suka belajar \"Java Programming\"");

        // \\ = menampilkan tanda backslash
        System.out.println("Folder tersimpan di C:\\Users\\Juniardi\\Documents");

        // \b = backspace (menghapus 1 karakter sebelum)
        System.out.println("Hello\b!");
    }
  }
